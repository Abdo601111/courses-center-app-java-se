    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        int answer = JOptionPane.showConfirmDialog(this, "Are you sure you wanna delete this screen?");
        if(answer == 0){
            if(screensTable.getSelectedRow() != -1){
                String code = (String)((DefaultTableModel)screensTable.getModel())
                        .getValueAt(screensTable.getSelectedRow(), 3);
                if(business.deleteScr(code)){
                    DefaultTableModel model =  (DefaultTableModel)screensTable.getModel();
                    model.removeRow(screensTable.getSelectedRow());
                    screensTable.setModel(model);
                    nameArTxt.setText("");
                    nameEnTxt.setText("");
                    JOptionPane.showMessageDialog(this, "Screen deleted successfuly :)");
                    selectedScreenId= -1;
                }else
                    JOptionPane.showMessageDialog(this, "Something went wrong please try again :(");
            }else{
                JOptionPane.showMessageDialog(this, "Please Select  Screen first!!");
            }
        }
    }//GEN-LAST:event_deleteBtnActionPerformed


