    private void initializeScreensTable() {
        try{
            List<ScreenDto> screens = business.listAllScreens(Constants.STATUS_LIST_ALL);
            Object [][] data = new Object[screens.size()][5];
            for(int i=0;i<screens.size();i++){
                data[i][0] = screens.get(i).getId();
                data[i][1] = screens.get(i).getNameAr();
                data[i][2] = screens.get(i).getNameEn();
                data[i][3] = screens.get(i).getCode();
                data[i][4] = screens.get(i).isActive();
            }
            
             
        
            DefaultTableModel model = new DefaultTableModel(data, headers);
            screensTable = new JTable(model){
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
                
                @Override
                public Class<?> getColumnClass(int column) {
                    switch(column){
                       case 0:
                           return Integer.class;
                       case 1:
                           return String.class;
                       case 2:
                           return String.class;
                       case 3:
                           return String.class;
                       case 4:
                           return Boolean.class;
                       default:
                           return Integer.class;
                   }   
                }
                
            };
               
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void updateScreenFormFromTable() {
        // TODO add your handling code here:
        try{
            int index = screensTable.getSelectedRow();
            selectedScreenId = Integer.parseInt(screensTable.getValueAt(index, 0).toString());
            nameArTxt.setText(screensTable.getValueAt(index, 1).toString());
            nameEnTxt.setText(screensTable.getValueAt(index, 2).toString());
            codeCombo.setSelectedItem(screensTable.getValueAt(index, 3).toString());
            activeChk.setSelected((Boolean)screensTable.getValueAt(index, 4));
            updateMode = true;
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Please Press Refresh First !!!");
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error:\n"+e.getMessage());
        }
    }
