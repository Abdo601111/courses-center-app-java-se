    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here
        try{
            //validation
            StringBuffer sb = new StringBuffer("");
            if(nameArTxt.getText().trim().equals(""))
                sb.append("- Arabic name is MUST.");
            if(nameEnTxt.getText().trim().equals("")){
                sb.append("- English name is MUST.");
            }
            if(!sb.toString().equals("")){
                errorLbl.setText(sb.toString());
                errorLbl.setVisible(true);
            }else{
                //call business to save
                errorLbl.setVisible(false);
                ScreenDto screen = null;
                screen = (selectedScreenId == -1)? 
                        new ScreenDto(codeCombo.getSelectedItem().toString(),nameArTxt.getText().trim(), nameEnTxt.getText().trim(), true)
                      : new ScreenDto(selectedScreenId, codeCombo.getSelectedItem().toString(),nameArTxt.getText().trim(), nameEnTxt.getText().trim(), true);
                if(business.save(screen)>0){
                    JOptionPane.showMessageDialog(this, "Screen have been added successfully :)");
                    //add newley inserted row to the screens table
                    if(selectedScreenId == -1){
                        DefaultTableModel model =  (DefaultTableModel)screensTable.getModel();
                        model.addRow(new Object[]{"", nameArTxt.getText(), nameEnTxt.getText(), codeCombo.getSelectedItem(), activeChk.isSelected()});
                        nameArTxt.setText("");
                        nameEnTxt.setText("");
                    }else{
                        DefaultTableModel model =  (DefaultTableModel)screensTable.getModel();
                        model.setValueAt(nameArTxt.getText(), screensTable.getSelectedRow(), 1);
                        model.setValueAt(nameEnTxt.getText(), screensTable.getSelectedRow(), 2);
                        model.setValueAt(codeCombo.getSelectedItem(), screensTable.getSelectedRow(), 3);
                        model.setValueAt(activeChk.isSelected(), screensTable.getSelectedRow(), 4);
                        screensTable.setModel(model);
                        selectedScreenId= -1;
                        nameArTxt.setText("");
                        nameEnTxt.setText("");
                    
                    }
                        
                }else
                    JOptionPane.showMessageDialog(this, "Something went wrong :( try agina later");
            }
                
        }catch(Exception e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error:\n"+e.getMessage());
        }
            
    }//GEN-LAST:event_jButton1ActionPerformed
